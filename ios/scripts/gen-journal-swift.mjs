// ============================================================
// gen-journal-swift.mjs — port the web journal into Swift
// ============================================================
// Reads src/data/journal-posts/*.md (the SAME files the website
// compiles) with the SAME parse rules as scripts/gen-journal-posts.mjs
// — YAML front-matter + p/h2/blockquote body nodes, sorted newest
// first with stable tie order — and writes the Swift data mirror at
// ios/LusikSons/Data/JournalPosts.swift.
//
// When posts change on main: copy the .md files onto this branch,
// then re-run from anywhere:  node ios/scripts/gen-journal-swift.mjs
// ============================================================

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const POSTS_DIR = join(__dirname, "..", "..", "src", "data", "journal-posts");
const OUT_FILE = join(__dirname, "..", "LusikSons", "Data", "JournalPosts.swift");

// ── parse logic: verbatim from scripts/gen-journal-posts.mjs ──

function parseFrontmatter(raw) {
  const m = raw.match(/^---\s*\n([\s\S]*?)\n---\s*\n([\s\S]*)$/);
  if (!m) return { meta: {}, body: raw };
  const meta = {};
  for (const line of m[1].split("\n")) {
    const kv = line.match(/^(\w+):\s*(.*)$/);
    if (!kv) continue;
    const key = kv[1];
    let value = kv[2].trim();
    if (value.startsWith('"') && value.endsWith('"')) value = value.slice(1, -1);
    meta[key] = value;
  }
  if (meta.readMinutes) meta.readMinutes = Number(meta.readMinutes);
  return { meta, body: m[2] };
}

function bodyToNodes(body) {
  const nodes = [];
  for (const para of body.trim().split(/\n\s*\n/)) {
    const t = para.trim();
    if (!t) continue;
    if (t.startsWith("## ")) nodes.push({ type: "h2", text: t.slice(3).trim() });
    else if (t.startsWith("> ")) nodes.push({ type: "blockquote", text: t.slice(2).trim() });
    else nodes.push({ type: "p", text: t.replace(/\n/g, " ") });
  }
  return nodes;
}

// readdir order is filesystem-dependent; the web generator relies on the
// usual alphabetical listing, so sort explicitly to pin the same stable
// tie order for posts sharing a publishedAt.
const files = readdirSync(POSTS_DIR).filter((f) => f.endsWith(".md")).sort();
const parsed = files.map((f) => {
  const { meta, body } = parseFrontmatter(readFileSync(join(POSTS_DIR, f), "utf8"));
  return {
    slug: meta.slug ?? "",
    title: meta.title ?? "",
    excerpt: meta.excerpt ?? "",
    publishedAt: meta.publishedAt ?? "",
    readMinutes: meta.readMinutes ?? 3,
    content: bodyToNodes(body),
  };
});
parsed.sort((a, b) => (a.publishedAt > b.publishedAt ? -1 : 1));

// ── Swift emission ──

// No trailing commas (only Swift 6.1+ toolchains parse them).
const str = (s) => `"${String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;

const postSwift = (p) => `        JournalPost(
            slug: ${str(p.slug)},
            title: ${str(p.title)},
            excerpt: ${str(p.excerpt)},
            publishedAt: ${str(p.publishedAt)},
            readMinutes: ${p.readMinutes},
            content: [
${p.content.map((n) => `                .${n.type === "p" ? "p" : n.type}(${str(n.text)})`).join(",\n")}
            ]
        )`;

const out = `// @generated by ios/scripts/gen-journal-swift.mjs — do NOT edit by hand.
// Source of truth: src/data/journal-posts/*.md (the same files the website
// compiles into journalPostsData.js). After editing posts, re-run:
//   node ios/scripts/gen-journal-swift.mjs
// Parse rules + ordering match scripts/gen-journal-posts.mjs exactly.

import Foundation

/// The ${parsed.length} journal posts, newest first — the Swift mirror of the web's
/// JOURNAL_POSTS array.
enum JournalPosts {
    static let all: [JournalPost] = [
${parsed.map(postSwift).join(",\n")}
    ]
}
`;

writeFileSync(OUT_FILE, out);
console.log(`gen-journal-swift: wrote ${parsed.length} posts → ${OUT_FILE}`);
