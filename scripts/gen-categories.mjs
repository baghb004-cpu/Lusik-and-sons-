// ============================================================
// gen-categories.mjs — build-time CMS codegen for shop categories
// ============================================================
// Reads content/categories/*.json (edited via the Content Studio at
// /studio → "Categories") and writes src/data/cmsCategoriesData.generated.js.
// src/data/catalog.js builds the CATALOG skeleton from it; products are
// merged in separately by gen-products.mjs output. Same contract as the
// sibling generators: invalid data THROWS and fails the build.
//
// Category slugs are LOAD-BEARING (URLs, product files reference them,
// sitemap). Renaming one breaks every product in it plus inbound links —
// the Studio hint says so, and the products generator's unknown-category
// check backstops it at build time.
// ============================================================

import { readdirSync, readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath, pathToFileURL } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const CATS_DIR = join(__dirname, "..", "content", "categories");
const OUT_FILE = join(__dirname, "..", "src", "data", "cmsCategoriesData.generated.js");

const SLUG_RE = /^[a-z0-9]+(?:-[a-z0-9]+)*$/;

function fail(file, msg) {
  throw new Error(`[gen-categories] ${file}: ${msg}`);
}

export function validateCategory(file, c) {
  for (const k of ["slug", "label", "description"]) {
    if (typeof c[k] !== "string" || c[k].trim() === "") fail(file, `"${k}" is required and must be a non-empty string`);
  }
  if (!SLUG_RE.test(c.slug)) fail(file, `"slug" must be lowercase kebab-case (got "${c.slug}")`);
  for (const k of ["label_hy", "description_hy", "eyebrow", "eyebrow_hy"]) {
    if (c[k] !== undefined && (typeof c[k] !== "string" || c[k].trim() === "")) {
      fail(file, `"${k}", if set, must be a non-empty string`);
    }
  }
  if (c.displayOrder !== undefined && typeof c.displayOrder !== "number") {
    fail(file, `"displayOrder", if set, must be a number`);
  }
}

function main() {
  const files = existsSync(CATS_DIR)
    ? readdirSync(CATS_DIR).filter((f) => f.endsWith(".json")).sort()
    : [];
  if (files.length === 0) fail("(none)", "no category files found — the shop cannot be empty");

  const raw = [];
  const seen = new Set();
  for (const file of files) {
    let data;
    try {
      data = JSON.parse(readFileSync(join(CATS_DIR, file), "utf8"));
    } catch (e) {
      fail(file, `invalid JSON — ${e.message}`);
    }
    validateCategory(file, data);
    if (seen.has(data.slug)) fail(file, `duplicate category slug "${data.slug}"`);
    seen.add(data.slug);
    raw.push(data);
  }

  // Shop order: displayOrder ascending (missing = last), tie-broken by slug.
  raw.sort((a, b) => (a.displayOrder ?? Infinity) - (b.displayOrder ?? Infinity) || a.slug.localeCompare(b.slug));

  // Strip sort-only metadata; emit the storefront shape (an ordered array —
  // catalog.js rebuilds its slug-keyed object from it).
  const out = raw.map(({ displayOrder, ...fields }) => fields);

  const banner =
    "// ============================================================\n" +
    "// AUTO-GENERATED by scripts/gen-categories.mjs — DO NOT EDIT.\n" +
    "// Source of truth: content/categories/*.json (Content Studio / Decap).\n" +
    "// Regenerate: `npm run gen:categories` (runs automatically prebuild).\n" +
    "// Consumed by src/data/catalog.js as the CATALOG skeleton.\n" +
    "// ============================================================\n";

  writeFileSync(OUT_FILE, `${banner}export const CMS_CATEGORIES = ${JSON.stringify(out, null, 2)};\n`);
  console.log(`gen-categories: wrote ${out.length} category(ies) → src/data/cmsCategoriesData.generated.js`);
}


// Run only when invoked directly (`node scripts/gen-X.mjs`) — the exported
// validator above is also imported by the builder's save gate
// (src/builder/server/validateDoc.ts), and importing must never regenerate.
const invokedDirectly = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (invokedDirectly) main();
