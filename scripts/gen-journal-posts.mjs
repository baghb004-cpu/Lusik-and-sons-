// ============================================================
// gen-journal-posts.mjs — build-time journal codegen
// ============================================================
// Reads src/data/journal-posts/*.md (YAML front-matter + markdown
// body) and writes src/data/journalPostsData.js as a plain, sorted
// JOURNAL_POSTS array.
//
// Why: the previous loader used Vite's `import.meta.glob`, which is
// Vite-only and unsupported by Next/webpack. Generating a plain data
// module makes the journal data portable across BOTH builds (Vite
// today, Next during the migration) with identical output and zero
// runtime glob. Runs as a prebuild step (predev / prebuild /
// prenext:build) so a Decap edit to an .md file is reflected on the
// next deploy exactly as before.
//
// The parse logic is copied verbatim from the old journalPosts.js so
// the emitted shape is byte-for-byte the same the components expect.
// ============================================================

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const POSTS_DIR = join(__dirname, "..", "src", "data", "journal-posts");
const OUT_FILE = join(__dirname, "..", "src", "data", "journalPostsData.js");

function parseFrontmatter(raw) {
  const m = raw.match(/^---\s*\n([\s\S]*?)\n---\s*\n([\s\S]*)$/);
  if (!m) return { meta: {}, body: raw };
  const meta = {};
  for (const line of m[1].split("\n")) {
    const kv = line.match(/^(\w+):\s*(.*)$/);
    if (!kv) continue;
    const key = kv[1];
    let value = kv[2].trim();
    if (value.startsWith('"') && value.endsWith('"')) value = value.slice(1, -1);
    meta[key] = value;
  }
  if (meta.readMinutes) meta.readMinutes = Number(meta.readMinutes);
  return { meta, body: m[2] };
}

function bodyToNodes(body) {
  const nodes = [];
  for (const para of body.trim().split(/\n\s*\n/)) {
    const t = para.trim();
    if (!t) continue;
    if (t.startsWith("## ")) nodes.push({ type: "h2", text: t.slice(3).trim() });
    else if (t.startsWith("> ")) nodes.push({ type: "blockquote", text: t.slice(2).trim() });
    else nodes.push({ type: "p", text: t.replace(/\n/g, " ") });
  }
  return nodes;
}

const files = readdirSync(POSTS_DIR).filter((f) => f.endsWith(".md"));
const parsed = files.map((f) => {
  const { meta, body } = parseFrontmatter(readFileSync(join(POSTS_DIR, f), "utf8"));
  return {
    slug:        meta.slug ?? "",
    title:       meta.title ?? "",
    excerpt:     meta.excerpt ?? "",
    publishedAt: meta.publishedAt ?? "",
    readMinutes: meta.readMinutes ?? 3,
    content:     bodyToNodes(body),
  };
});
parsed.sort((a, b) => (a.publishedAt > b.publishedAt ? -1 : 1));

const banner =
  "// @generated by scripts/gen-journal-posts.mjs from src/data/journal-posts/*.md\n" +
  "// Do NOT edit by hand — edit the .md files (or use Decap at /admin/) and rebuild.\n";
writeFileSync(OUT_FILE, banner + "export const JOURNAL_POSTS = " + JSON.stringify(parsed, null, 2) + ";\n");
console.log(`gen-journal-posts: wrote ${parsed.length} posts → ${OUT_FILE}`);
