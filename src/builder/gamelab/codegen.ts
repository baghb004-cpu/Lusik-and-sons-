// A deliberately small 3D starter: walk a cube with the arrow keys on a ground
// plane and collect/avoid the other cubes. Placeholders are BoxMeshes — swap in
// your own .glb models later. Kept simple (no physics) like the 2D path.
function mainGd3d(p: GameProject): string {
  const settings = JSON.stringify(p.settings);
  const entities = JSON.stringify(p.entities.map(entityLite));
  return [
    "extends Node3D",
    `# ============================================================`,
    `# ${p.name} — a simple 3D starter made in Game Lab`,
    `# Walk with the arrow keys. Collect coins, reach the goal, avoid enemies/spikes.`,
    `# The cubes are placeholders — drop your own .glb models in res://assets later.`,
    `# ============================================================`,
    "",
    `const SETTINGS := ${settings}`,
    `const ENTITIES := ${entities}  # x,y are used as x,z on the ground plane`,
    "",
    "var score: int = 0",
    'var health: int = int(SETTINGS.get("playerHealth", 3))',
    "var finished: bool = false",
    "var player: MeshInstance3D = null",
    "var nodes: Array = []",
    "var alive: Array = []",
    "var hud: Label = null",
    "",
    "func _ready() -> void:",
    "\t_make_light(); _make_ground(); _make_camera()",
    "\tfor e in ENTITIES:",
    '\t\tvar is_player: bool = e["type"] == "player"',
    "\t\tvar m := _box(Vector3(0.8, 0.8, 0.8) if is_player else Vector3(0.6, 0.6, 0.6), Color(e[\"color\"]))",
    "\t\tm.position = _world(e)",
    "\t\tadd_child(m); nodes.append(m); alive.append(true)",
    "\t\tif is_player: player = m",
    "\t_make_hud()",
    "",
    "func _world(e: Dictionary) -> Vector3:",
    '\treturn Vector3(float(e["x"]) / 64.0 - 5.0, 0.4, float(e["y"]) / 64.0 - 3.0)',
    "",
    "func _box(size: Vector3, col: Color) -> MeshInstance3D:",
    "\tvar mi := MeshInstance3D.new()",
    "\tvar bm := BoxMesh.new(); bm.size = size; mi.mesh = bm",
    "\tvar mat := StandardMaterial3D.new(); mat.albedo_color = col; mi.material_override = mat",
    "\treturn mi",
    "",
    "func _make_ground() -> void:",
    '\tvar g := _box(Vector3(20, 0.2, 14), Color("#1f2937")); g.position = Vector3(0, -0.1, 0); add_child(g)',
    "",
    "func _make_light() -> void:",
    "\tvar l := DirectionalLight3D.new(); l.rotation_degrees = Vector3(-55, -30, 0); add_child(l)",
    "",
    "func _make_camera() -> void:",
    "\tvar c := Camera3D.new(); c.position = Vector3(0, 9, 9); c.look_at(Vector3.ZERO, Vector3.UP); add_child(c)",
    "",
    "func _make_hud() -> void:",
    "\tvar layer := CanvasLayer.new(); add_child(layer)",
    "\thud = Label.new(); hud.position = Vector2(12, 8); layer.add_child(hud); _update_hud()",
    "",
    "func _update_hud() -> void:",
    '\thud.text = "Score: %d   Health: %d" % [score, health]',
    "",
    "func _process(delta: float) -> void:",
    "\tif finished or player == null: return",
    '\tvar speed: float = float(SETTINGS.get("playerSpeed", 200)) / 40.0  # pixel-ish → 3D units',
    '\tvar v := Input.get_vector("ui_left", "ui_right", "ui_up", "ui_down")',
    "\tplayer.position += Vector3(v.x, 0, v.y) * speed * delta",
    "\tfor i in ENTITIES.size():",
    "\t\tif not alive[i]: continue",
    '\t\tif ENTITIES[i]["type"] == "player": continue',
    "\t\tif player.position.distance_to(nodes[i].position) < 0.9:",
    '\t\t\t_touch(ENTITIES[i]["type"], i)',
    "\t_update_hud()",
    "",
    "func _touch(t: String, i: int) -> void:",
    "\tmatch t:",
    '\t\t"coin":',
    "\t\t\tscore += 1; alive[i] = false; nodes[i].visible = false",
    '\t\t"enemy", "spike":',
    "\t\t\thealth -= 1",
    "\t\t\tif health <= 0: _end(false)",
    '\t\t"goal":',
    "\t\t\t_end(true)",
    "",
    "func _end(win: bool) -> void:",
    "\tif finished: return",
    "\tfinished = true",
    "\tvar layer := CanvasLayer.new(); add_child(layer)",
    '\tvar b := Label.new(); b.position = Vector2(220, 150); b.text = "You win!" if win else "Game over"; layer.add_child(b)',
    "",
  ].join("\n");
}

