// ============================================================
// Game Lab — Godot project code generator (pure)
// ============================================================
// Turns a GameProject into a real, runnable Godot 4 project as a file
// map: project.godot, a minimal Main.tscn (root + script), a single
// COMMENTED GDScript that builds the level from an embedded config, the
// re-importable game_config.json, plus README + LICENSES. Visuals are
// placeholder ColorRects — no copyrighted assets, ever. The config is
// embedded as JSON, which is valid GDScript literal syntax.
// ============================================================

import type { GameProject, Entity } from "./schemas.ts";

const VIEW_W = 640;
const VIEW_H = 360;

function projectGodot(p: GameProject): string {
  return [
    "; Generated by Game Lab — an original game, yours to edit.",
    "config_version=5",
    "",
    "[application]",
    `config/name=${JSON.stringify(p.name)}`,
    'run/main_scene="res://scenes/Main.tscn"',
    'config/features=PackedStringArray("4.3", "GL Compatibility")',
    "",
    "[display]",
    `window/size/viewport_width=${VIEW_W}`,
    `window/size/viewport_height=${VIEW_H}`,
    "",
    "[rendering]",
    'renderer/rendering_method="gl_compatibility"',
    "",
  ].join("\n");
}

function mainTscn(): string {
  return [
    '[gd_scene load_steps=2 format=3 uid="uid://gamelabmain"]',
    "",
    '[ext_resource type="Script" path="res://scripts/Main.gd" id="1_main"]',
    "",
    '[node name="Main" type="Node2D"]',
    'script = ExtResource("1_main")',
    "",
  ].join("\n");
}

// Minimal entity view the runtime needs (placeholder visuals + behavior tags).
// `sprite` (an imported image filename in res://assets) is lifted to the top
// level so the generated script can swap a TextureRect in for the placeholder.
function entityLite(e: Entity) {
  const sprite = typeof e.props.sprite === "string" ? e.props.sprite : "";
  return { type: e.type, name: e.name, x: e.x, y: e.y, w: e.w, h: e.h, color: e.color, sprite };
}

function mainGd(p: GameProject): string {
  const settings = JSON.stringify(p.settings);
  const entities = JSON.stringify(p.entities.map(entityLite));
  const rules = JSON.stringify(p.rules.map((r) => ({ when: r.when, then: r.then, params: r.params })));
  const L: string[] = [];
  const push = (...lines: string[]) => L.push(...lines);

  push(
    "extends Node2D",
    `# ============================================================`,
    `# ${p.name} — a ${p.kind.replace("-", " ")} made in Game Lab`,
    `# This is YOUR game. Edit anything. The colored rectangles are`,
    `# placeholders — drop your own images in res://assets and swap them in.`,
    `# Movement uses Godot's built-in ui_left/right/up/down + ui_accept,`,
    `# so it works with arrow keys / WASD-mapped keys and a gamepad.`,
    `# ============================================================`,
    "",
    `const KIND := ${JSON.stringify(p.kind)}`,
    `const SETTINGS := ${settings}`,
    `const ENTITIES := ${entities}`,
    `const RULES := ${rules}`,
    "",
    "var score: int = 0",
    'var health: int = int(SETTINGS.get("playerHealth", 3))',
    'var time_left: float = float(SETTINGS.get("timeLimit", 0))',
    "var finished: bool = false",
    "var player: Control = null",
    "var vel: Vector2 = Vector2.ZERO",
    "var nodes: Array = []        # one Control per entity (parallel to ENTITIES)",
    "var alive: Array = []        # is each entity still in play",
    "var shots: Array = []        # active projectiles (space shooter)",
    "var hud: Label = null",
    "var banner: Label = null",
    "",
    "func _ready() -> void:",
    "\t_build_scene()",
    "\t_build_hud()",
    "\tfor r in RULES:",
    '\t\tif r["then"] == "playMusic":',
    '\t\t\tpass  # Drop a track in res://audio and play it here (left off by default).',
    "",
    "func _build_scene() -> void:",
    "\tfor e in ENTITIES:",
    "\t\tvar r: Control",
    '\t\tif e.has("sprite") and String(e["sprite"]) != "":',
    "\t\t\t# Your imported image (placeholder is used if it fails to load).",
    "\t\t\tvar tr := TextureRect.new()",
    '\t\t\tvar tex = load("res://assets/" + String(e["sprite"]))',
    "\t\t\tif tex: tr.texture = tex",
    "\t\t\ttr.expand_mode = TextureRect.EXPAND_IGNORE_SIZE",
    "\t\t\tr = tr",
    "\t\telse:",
    "\t\t\tvar cr := ColorRect.new()",
    '\t\t\tcr.color = Color(e["color"])',
    "\t\t\tr = cr",
    '\t\tr.size = Vector2(e["w"], e["h"])',
    '\t\tr.position = Vector2(e["x"], e["y"])',
    "\t\tadd_child(r)",
    "\t\tnodes.append(r)",
    "\t\talive.append(true)",
    '\t\tif e["type"] == "player":',
    "\t\t\tplayer = r",
    "",
    "func _build_hud() -> void:",
    "\tvar layer := CanvasLayer.new()",
    "\tadd_child(layer)",
    "\thud = Label.new()",
    "\thud.position = Vector2(12, 8)",
    "\tlayer.add_child(hud)",
    "\tbanner = Label.new()",
    "\tbanner.position = Vector2(220, 150)",
    "\tbanner.visible = false",
    "\tlayer.add_child(banner)",
    "\t_update_hud()",
    "",
    "func _update_hud() -> void:",
    '\tvar parts := []',
    '\tif SETTINGS.has("scoreGoal"):',
    '\t\tparts.append("Score: %d / %d" % [score, int(SETTINGS["scoreGoal"])])',
    "\telse:",
    '\t\tparts.append("Score: %d" % score)',
    "\tif _ui_health():",
    '\t\tparts.append("Health: %d" % health)',
    '\tif time_left > 0.0:',
    '\t\tparts.append("Time: %d" % int(ceil(time_left)))',
    '\thud.text = "   ".join(parts)',
    "",
    "func _ui_health() -> bool:",
    `\treturn ${p.ui.health ? "true" : "false"}`,
    "",
    "func _process(delta: float) -> void:",
    "\tif finished or player == null:",
    "\t\treturn",
    "\tmatch KIND:",
    '\t\t"platformer":',
    "\t\t\t_move_platformer(delta, false)",
    '\t\t"endless-runner":',
    "\t\t\t_move_platformer(delta, true)",
    '\t\t"top-down":',
    "\t\t\t_move_topdown(delta)",
    '\t\t"puzzle":',
    "\t\t\t_move_topdown(delta)",
    "\t\t\t_check_buttons()",
    '\t\t"dodge":',
    "\t\t\t_move_sideways(delta, false)",
    '\t\t"space-shooter":',
    "\t\t\t_move_sideways(delta, true)",
    "\t_check_overlaps()",
    "\t_tick_timer(delta)",
    "\t_update_hud()",
    "",
    "func _move_platformer(delta: float, auto: bool) -> void:",
    '\tvar speed: float = float(SETTINGS.get("playerSpeed", 200))',
    '\tvar gravity: float = float(SETTINGS.get("gravity", 980))',
    '\tvar jump: float = float(SETTINGS.get("jumpForce", 420))',
    '\t# Endless runner: auto-run right and wrap around, scoring distance.',
    '\tvar dir := 1.0 if auto else Input.get_axis("ui_left", "ui_right")',
    "\tplayer.position.x += dir * speed * delta",
    "\tif auto and player.position.x > get_viewport_rect().size.x:",
    "\t\tplayer.position.x = 0.0",
    "\t\tscore += 1",
    "\tvel.y += gravity * delta",
    "\tplayer.position.y += vel.y * delta",
    "\tvar on_ground := false",
    "\tfor i in ENTITIES.size():",
    "\t\tif not alive[i]: continue",
    '\t\tvar t = ENTITIES[i]["type"]',
    '\t\tif t in ["platform", "wall", "movingPlatform", "door"]:',
    "\t\t\tvar pr: Control = nodes[i]",
    "\t\t\tif _falling_onto(player, pr):",
    "\t\t\t\tplayer.position.y = pr.position.y - player.size.y",
    "\t\t\t\tvel.y = 0.0",
    "\t\t\t\ton_ground = true",
    '\tif on_ground and Input.is_action_just_pressed("ui_accept"):',
    "\t\tvel.y = -jump",
    "",
    "func _move_topdown(delta: float) -> void:",
    '\tvar speed: float = float(SETTINGS.get("playerSpeed", 180))',
    '\tvar v := Input.get_vector("ui_left", "ui_right", "ui_up", "ui_down")',
    "\tvar next := player.position + v * speed * delta",
    "\tvar old := player.position",
    "\tplayer.position = next",
    "\tfor i in ENTITIES.size():",
    "\t\tif not alive[i]: continue",
    '\t\tvar t = ENTITIES[i]["type"]',
    '\t\tif t in ["wall", "door"] and _overlap(player, nodes[i]):',
    "\t\t\tplayer.position = old  # blocked",
    "\t\t\tbreak",
    "",
    "func _move_sideways(delta: float, shoot: bool) -> void:",
    '\tvar speed: float = float(SETTINGS.get("playerSpeed", 240))',
    '\tvar dir := Input.get_axis("ui_left", "ui_right")',
    "\tplayer.position.x += dir * speed * delta",
    "\tplayer.position.x = clamp(player.position.x, 0.0, float(get_viewport_rect().size.x) - player.size.x)",
    "\t# Hazards drift down and wrap back to the top (dodge / descending enemies).",
    '\tvar fall: float = 120.0 * float(SETTINGS.get("spawnRate", 1))',
    "\tfor i in ENTITIES.size():",
    "\t\tif not alive[i]: continue",
    '\t\tif ENTITIES[i]["type"] in ["spike", "enemy", "projectile"]:',
    "\t\t\tnodes[i].position.y += fall * delta",
    "\t\t\tif nodes[i].position.y > get_viewport_rect().size.y:",
    "\t\t\t\tnodes[i].position.y = -nodes[i].size.y",
    "\tif shoot:",
    '\t\tif Input.is_action_just_pressed("ui_accept"):',
    "\t\t\t_fire()",
    "\t\t_update_shots(delta)",
    "",
    "func _fire() -> void:",
    "\tvar b := ColorRect.new()",
    '\tb.color = Color("#f97316")',
    "\tb.size = Vector2(6, 14)",
    "\tb.position = Vector2(player.position.x + player.size.x / 2.0 - 3.0, player.position.y - 14.0)",
    "\tadd_child(b)",
    "\tshots.append(b)",
    "",
    "func _update_shots(delta: float) -> void:",
    "\tfor b in shots.duplicate():",
    "\t\tif not is_instance_valid(b): continue",
    "\t\tb.position.y -= 500.0 * delta",
    "\t\tif b.position.y < -20.0:",
    "\t\t\tshots.erase(b); b.queue_free(); continue",
    "\t\tfor i in ENTITIES.size():",
    '\t\t\tif alive[i] and ENTITIES[i]["type"] == "enemy" and _overlap(b, nodes[i]):',
    "\t\t\t\talive[i] = false; nodes[i].visible = false",
    "\t\t\t\tshots.erase(b); b.queue_free()",
    "\t\t\t\tscore += 1; _check_score_goal()",
    "\t\t\t\tbreak",
    "",
    "func _check_buttons() -> void:",
    "\tfor i in ENTITIES.size():",
    '\t\tif alive[i] and ENTITIES[i]["type"] == "button" and _overlap(player, nodes[i]):',
    '\t\t\t_do("buttonPressed")',
    "",
    "func _check_overlaps() -> void:",
    "\tfor i in ENTITIES.size():",
    "\t\tif not alive[i]: continue",
    '\t\tif ENTITIES[i]["type"] == "player": continue',
    "\t\tif _overlap(player, nodes[i]):",
    '\t\t\t_on_touch(ENTITIES[i]["type"], i)',
    "",
    "func _on_touch(t: String, i: int) -> void:",
    "\tmatch t:",
    '\t\t"coin":',
    '\t\t\t_do("touchCoin"); _collect(i)',
    '\t\t"spike":',
    '\t\t\t_do("touchSpike")',
    '\t\t"enemy":',
    '\t\t\t_do("touchEnemy")',
    '\t\t"key":',
    '\t\t\t_do("touchKey"); _collect(i)',
    '\t\t"health":',
    '\t\t\t_do("touchHealth"); _collect(i)',
    '\t\t"goal":',
    '\t\t\t_do("touchGoal")',
    "",
    "func _do(ev: String) -> void:",
    "\tfor r in RULES:",
    '\t\tif r["when"] != ev: continue',
    '\t\tmatch r["then"]:',
    '\t\t\t"addScore":',
    '\t\t\t\tscore += int(r["params"].get("points", 1)); _check_score_goal()',
    '\t\t\t"loseHealth":',
    '\t\t\t\tif ev == "touchHealth": health += 1',
    '\t\t\t\telse: health -= int(r["params"].get("amount", 1))',
    "\t\t\t\tif health <= 0: _end(false)",
    '\t\t\t"gameOver":',
    "\t\t\t\t_end(false)",
    '\t\t\t"winLevel", "showVictory":',
    "\t\t\t\t_end(true)",
    '\t\t\t"openGate", "unlockDoor":',
    "\t\t\t\t_open_gates()",
    "",
    "func _open_gates() -> void:",
    "\tfor i in ENTITIES.size():",
    '\t\tif ENTITIES[i]["type"] == "door":',
    "\t\t\talive[i] = false",
    "\t\t\tnodes[i].visible = false",
    "",
    "func _check_score_goal() -> void:",
    '\tif SETTINGS.has("scoreGoal") and int(SETTINGS["scoreGoal"]) > 0 and score >= int(SETTINGS["scoreGoal"]):',
    "\t\tfor r in RULES:",
    '\t\t\tif r["when"] == "scoreReached" and r["then"] in ["showVictory", "winLevel"]:',
    "\t\t\t\t_end(true)",
    "",
    "func _tick_timer(delta: float) -> void:",
    "\tif time_left <= 0.0: return",
    "\ttime_left -= delta",
    "\tif time_left <= 0.0:",
    "\t\ttime_left = 0.0",
    "\t\tfor r in RULES:",
    '\t\t\tif r["when"] == "timerZero":',
    '\t\t\t\t_end(r["then"] in ["showVictory", "winLevel"])',
    "",
    "func _collect(i: int) -> void:",
    "\talive[i] = false",
    "\tnodes[i].visible = false",
    "",
    "func _end(win: bool) -> void:",
    "\tif finished: return",
    "\tfinished = true",
    '\tbanner.text = "You win!" if win else "Game over"',
    "\tbanner.visible = true",
    "",
    "# --- tiny AABB helpers (no physics engine needed for a mini game) ---",
    "func _overlap(a: Control, b: Control) -> bool:",
    "\treturn a.position.x < b.position.x + b.size.x and a.position.x + a.size.x > b.position.x \\",
    "\t\tand a.position.y < b.position.y + b.size.y and a.position.y + a.size.y > b.position.y",
    "",
    "func _falling_onto(a: Control, b: Control) -> bool:",
    "\t# a is landing on top of b when falling and horizontally overlapping",
    "\tvar horizontal := a.position.x < b.position.x + b.size.x and a.position.x + a.size.x > b.position.x",
    "\tvar feet := a.position.y + a.size.y",
    "\treturn vel.y >= 0.0 and horizontal and feet >= b.position.y and feet <= b.position.y + 24.0",
    "",
  );

  // Click-based kinds get input handling instead of movement.
  if (["clicker", "quiz", "memory"].includes(p.kind)) {
    push(
      "func _unhandled_input(event: InputEvent) -> void:",
      "\tif finished: return",
      "\tif event is InputEventMouseButton and event.pressed:",
      "\t\tfor i in ENTITIES.size():",
      "\t\t\tif not alive[i]: continue",
      '\t\t\tif ENTITIES[i]["type"] in ["button", "card", "question"]:',
      "\t\t\t\tvar n: Control = nodes[i]",
      "\t\t\t\tvar r := Rect2(n.position, n.size)",
      "\t\t\t\tif r.has_point(event.position):",
      '\t\t\t\t\t_do("objectClicked")',
      "\t\t\t\t\t_update_hud()",
      "",
    );
  }

  return L.join("\n") + "\n";
}

